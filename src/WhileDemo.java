public class WhileDemo {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("hello world");
        }

        System.out.println("----------");

        int j = 1;
        while (j <= 5) {
            System.out.println("hello world");
            j++;
        }
    }
}
