public class IfDemo {
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        if (a == b ){
            System.out.println("a==b");
        }

        int c = 10;
        if (a == c ){
            System.out.println("a==c");
        }

        System.out.println("end");
    }
}
