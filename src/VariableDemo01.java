/*
    变量定义格式：
        数据类型 变量名 = 变量值;
    基本数据类型：
        byte, short, int, long, float, double, char, boolean
    变量的使用：
        取值格式：变量名
        修改值格式：变量名 = 变量值;
 */
public class VariableDemo01 {
    public static void main(String[] args){
        int a = 10;
        System.out.println(a);
        a = 20;
        System.out.println(a);
    }
}
